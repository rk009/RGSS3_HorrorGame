#==============================================================================
# ■ Window_Description
#------------------------------------------------------------------------------
# 　画面に説明を表示する。
#==============================================================================

class Window_Description < Window_Line
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize(y)
    super(y)
  end
  #--------------------------------------------------------------------------
  # ● テキスト描画
  #--------------------------------------------------------------------------
  def create_text
    rect = contents.rect
    rect.x += 20
    draw_text(rect, @text)
  end
  #--------------------------------------------------------------------------
  # ● 背景作成
  #--------------------------------------------------------------------------
  def create_background
    rect = Rect.new(0, 20, item_width, 20)
    contents.gradient_fill_rect(rect, Color.new(0,0,0,200), Color.new(0,0,0,0))
  end
  #--------------------------------------------------------------------------
  # ● フォント名
  #--------------------------------------------------------------------------
  def font_name
    return "HGP明朝E"
  end
  #--------------------------------------------------------------------------
  # ● フォントサイズ
  #--------------------------------------------------------------------------
  def font_size
    return 16
  end
end