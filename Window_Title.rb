#==============================================================================
# ■ Window_Title
#------------------------------------------------------------------------------
# 　画面上部にタイトルを表示する
#==============================================================================

class Window_Title < Window_Line
  #--------------------------------------------------------------------------
  # ● オブジェクト初期化
  #--------------------------------------------------------------------------
  def initialize
    super(0)
  end
  #--------------------------------------------------------------------------
  # ● テキスト描画
  #--------------------------------------------------------------------------
  def create_text
    rect = contents.rect
    rect.x += 10
    draw_text(rect, @text)
  end
  #--------------------------------------------------------------------------
  # ● フォント名
  #--------------------------------------------------------------------------
  def font_name
    return "HGP明朝E"
  end
  #--------------------------------------------------------------------------
  # ● フォントサイズ
  #--------------------------------------------------------------------------
  def font_size
    return 30
  end
  #--------------------------------------------------------------------------
  # ● 文字カラー
  #--------------------------------------------------------------------------
  def font_color
    return 15
  end
  #--------------------------------------------------------------------------
  # ● 文字太字
  #--------------------------------------------------------------------------
  def font_bold?
    return true
  end
end